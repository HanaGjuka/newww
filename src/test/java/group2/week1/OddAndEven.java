package test.java.group2.week1;

public class OddAndEven {

    public static void main(String[] args) {

        oddOrEven(5);

    }


    public static void oddOrEven(int num){

        if( num % 2 ==0){
            System.out.println(num + " is even number ");
        }else {
            System.out.println(num + " is odd number ");
        }

    }



}
